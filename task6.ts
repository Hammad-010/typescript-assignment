
const personNameWithWhitespace = "\t  Hammad Khan\n"; // Example name with whitespace

// Print the name with whitespace
console.log("Name with Whitespace:");
console.log(personNameWithWhitespace);

// Strip the whitespace and print the name without it
const strippedName = personNameWithWhitespace.trim();
console.log("Name without Whitespace:");
console.log(strippedName);
